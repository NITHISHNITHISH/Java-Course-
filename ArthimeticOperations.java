package first;

public class ArthimeticOperations {

	public static void main(String[] args) {

		// We can perform Arthimetic operation on char
		char ch1='a';
		char ch2='b';
		int result = ch1+ch2;
		System.out.println(result);
		
		// += addition Assignment
		
		int a = 5;
		System.out.println("before a = "+a);
			a+=10; // full form a = a + 10;
		System.out.println("after a = "+a);
			
		// -= addition Assignment
		
				int b = 50;
				System.out.println("before b = "+b);
					b-=10;
				System.out.println("after b = "+b);
				
		// *= addition Assignment
				
				int c = 5;
				System.out.println("before c = "+c);
					c*=10;
				System.out.println("after c = "+c);
				
				
		// increment operator
				
				int d=5;
				System.out.println("Before d= "+d);
				d++;
				System.out.println("After d= "+d);
		
		
	}

}
